# 5. Conversão de Medidas – Metros para Centímetros
# Desenvolva um programa que receba um valor em metros e o converta para centímetros.
# Sabemos que:
# O programa deve exibir o resultado da conversão.

meters = float(input("Digite um valor em metros: "))
centimeters = meters * 100
print(f"{meters} metros é igual a {centimeters} centímetros")